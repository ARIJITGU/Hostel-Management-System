    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String mobile_number = jTextField1.getText();
        String name = jTextField2.getText().toUpperCase();
        String father_name = jTextField3.getText().toUpperCase();
        String mother_name = jTextField4.getText().toUpperCase();
        String e_mail = jTextField5.getText();
        String address = jTextField6.getText().toUpperCase();
        String addhar_number = jTextField7.getText();
        String designation = (String) jComboBox1.getSelectedItem();
        String status = "Working";
        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("insert into emp(mobile_number,name,father_name,mother_name,e_mail,address,addhar_number,designation,status)values(?,?,?,?,?,?,?,?,?)");
            ps.setString(1, mobile_number);
            ps.setString(2, name);
            ps.setString(3, father_name);
            ps.setString(4, mother_name);
            ps.setString(5, e_mail);
            ps.setString(6, address);
            ps.setString(7, addhar_number);
            ps.setString(8, designation);
            ps.setString(9, status);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "INSERTED SUCCESSFULLY");
            clear();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

